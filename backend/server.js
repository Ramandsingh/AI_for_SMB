async function runMigrations(db) {
  await db.query(`
    CREATE TABLE IF NOT EXISTS companies (
      id           INT AUTO_INCREMENT PRIMARY KEY,
      name         VARCHAR(200) NOT NULL,
      industry     VARCHAR(100),
      size         VARCHAR(50),
      contact_name VARCHAR(100),
      notes        TEXT,
      created_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP
    )
  `);

  const [aCols] = await db.query(`SHOW COLUMNS FROM assessments LIKE 'company_id'`);
  if (!aCols.length) {
    await db.query(`ALTER TABLE assessments ADD COLUMN company_id INT, ADD CONSTRAINT fk_assess_company FOREIGN KEY (company_id) REFERENCES companies(id) ON DELETE SET NULL`);
  }

  const [rCols] = await db.query(`SHOW COLUMNS FROM roi_calculations LIKE 'company_id'`);
  if (!rCols.length) {
    await db.query(`ALTER TABLE roi_calculations ADD COLUMN company_id INT, ADD CONSTRAINT fk_roi_company FOREIGN KEY (company_id) REFERENCES companies(id) ON DELETE SET NULL`);
  }

  await db.query(`
    CREATE TABLE IF NOT EXISTS enterprise_functions (
      id         INT AUTO_INCREMENT PRIMARY KEY,
      name       VARCHAR(255) NOT NULL,
      icon_name  VARCHAR(100) NOT NULL DEFAULT 'Megaphone',
      color      VARCHAR(100) NOT NULL DEFAULT 'border-l-blue-400',
      maturity   VARCHAR(50)  NOT NULL DEFAULT 'High',
      stat       TEXT,
      uses       JSON         NOT NULL,
      sort_order INT          DEFAULT 0,
      updated_at TIMESTAMP    DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
    )
  `);

  const [[{ count }]] = await db.query('SELECT COUNT(*) AS count FROM enterprise_functions');
  if (count === 0) {
    for (const fn of ENTERPRISE_FUNCTIONS_SEED) {
      await db.query(
        `INSERT INTO enterprise_functions (name, icon_name, color, maturity, stat, uses, sort_order)
         VALUES (?, ?, ?, ?, ?, ?, ?)`,
        [fn.name, fn.icon_name, fn.color, fn.maturity, fn.stat, JSON.stringify(fn.uses), fn.sort_order]
      );
    }
    console.log('enterprise_functions seeded');
  }
}

