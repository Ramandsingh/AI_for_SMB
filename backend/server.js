app.get('/api/enterprise/functions', async (req, res) => {
  try {
    const db = await getPool();
    const [rows] = await db.query('SELECT * FROM enterprise_functions ORDER BY sort_order');
    const parsed = rows.map(r => ({ ...r, uses: typeof r.uses === 'string' ? JSON.parse(r.uses) : r.uses }));
    res.json(parsed);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

app.put('/api/enterprise/functions/:id', async (req, res) => {
  const { id } = req.params;
  const { name, icon_name, color, maturity, stat, uses } = req.body;
  if (!name || !Array.isArray(uses)) return res.status(400).json({ error: 'name and uses (array) are required' });
  try {
    const db = await getPool();
    await db.query(
      `UPDATE enterprise_functions SET name=?, icon_name=?, color=?, maturity=?, stat=?, uses=? WHERE id=?`,
      [name, icon_name, color, maturity, stat, JSON.stringify(uses), id]
    );
    const [[row]] = await db.query('SELECT * FROM enterprise_functions WHERE id=?', [id]);
    if (!row) return res.status(404).json({ error: 'Not found' });
    res.json({ ...row, uses: typeof row.uses === 'string' ? JSON.parse(row.uses) : row.uses });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

